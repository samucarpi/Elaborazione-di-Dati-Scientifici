function [desgn, col_ID, alias_ID, res] = ffacdes1(k,p,options)
%FACTDES Fractional factorial 2-level design of experiments.
% Generates a fractional factorial design of experiments (DOE) for
% two-level designs given the number of factors (fact) and fraction desired
% (p). Outputs include information on the aliasing of factors and
% resolution of the design.
%
% INPUTS:
%  k = total number of factors in the design
%  p = numerical indicator of the fraction desired (default = 1)
%      1 : Half Fraction
%      2 : Quarter Fraction
%      3 : Eighth Fraction
%      4 : Sixteenth Fraction
%        Note: Only fractionation up to 1/16 presently supported
%
% OPTIONAL INPUTS:
%  options  = Structure array with one or more of the following fields:
%         display : [ 'off' |{'on'}] governs display of DOE information
%
% OUTPUTS:
%  If the dso option is false, the outputs include:
%
%  desgn     = is a matrix of the experimental design in uncoded form
%  col_ID    = is a cell array of strings describing the multiplicative
%              origin of each column; one col for each coefficient to
%              potentially be calculated where:
%         * the first k cells describe the original main factors
%         * the remaining cells describe the various interactions among main
%          factors 
%
%  alias_ID  = is a cell array of logicals describing the alias structure
%              of the selected design; one row per coefficient/X-column.
%              Multiplying a given logical by the full set of characters
%              representing the factors yields the alias relationship for
%              that row/coefficient.
%             Ex.  ABCDEF .* [1 0 0 1 1 0] = ADE
%  res       = resolution of the selected design.
%
%I/O: [desgn, col_ID, alias_ID, res] = ffacdes1(k,p,options)
%
%See also: BOXBEHNKEN, CCDFACE, CCDSPHERE, DOEGEN, DOESCALE, FACTDES

%Copyright Eigenvector Research, Inc. 2011
%Licensee shall not re-compile, translate or convert "M-files" contained
% in PLS_Toolbox for use with any software other than MATLAB®, without
% written permission from Eigenvector Research, Inc.


if nargin == 0; k = 'io'; end
if ischar(k);
  options = [];
  options.display = 'on';
  if nargout==0; evriio(mfilename,k,options); else; desgn = evriio(mfilename,k,options); end
  return; 
end

if nargin<2
  p    = 0; %generate the FULL FACTORIAL if p not specified
end
if (p<0 | k<0)
  error('All inputs must be > 0')
end
if p >= k
  error('p must be less than k')
end

if nargin<3;
  options = [];
end
options = reconopts(options,mfilename);

%================================
% Reference tables from Box-Hunter
%'frac_table' hold defining relationships for common fractional factorials
%'resol' hold the corresponding resolution for the common fract. factorials
% using a cell array of cell arrays, 
% dims are #runs(unused) by #factors

desgn_gen  = get_design_gen;
frac_table = get_frac_table;
alias      = get_alias;
resol      = get_resol;

%================================

%test to see if user selection is allowed and/or available
if p~=0 %skip .... user wants full factorial
  indx = [k-p-1 k-2];  %where can we find this DOE in the frac_table?
  if any(size(frac_table) < indx) | any(indx<1) | isempty(frac_table{indx(1),indx(2)})
    %don't have an entry OR entry is EMPTY
    error('Selected fractional design not allowed or not available')
  end
end

desgn  = factdes(k-p); %Get base factorial design; k-p columns

if p ~= 0   %if p=0, skip the following and dump out full factorial design
    for ii = 1:p    % p indicates the # of defining relationships/generators
      %extract and apply defining relation|ships
      ind = desgn_gen{k-p-1,k-2}{ii}; %which block w/in frac_table, then which specific generator
      desgn = [desgn prod(desgn(:,ind),2)];
    end
end

%Populate variables with values specific to this design
col_ID    = frac_table{k-p-1,k-2}; %get full X-col definitions from frac_table
alias_ID  = alias{k-p-1,k-2};
res       = resol{k-p-1,k-2};

if strcmpi(options.display,'on')
  %Display design information=======
  disp('===Design Information===');
  if p~=0
    disp(sprintf('2^%d-%d Design',k,p))
  end
  switch p
    case 0
      disp('Full Factorial');
    case 1
      disp('1/2 Fraction');
    case 2
      disp('1/4 Fraction');
    case 3
      disp('1/8 Fraction');
    case 4
      disp('1/16 Fraction');
    case 5
      disp('1/32 Fraction');
    case 6
      disp('1/64 Fraction');
    case 7
      disp('1/128 Fraction');
  end
  disp(res);
  disp([num2str(size(desgn,1)) ' Total Experiments']);
end

